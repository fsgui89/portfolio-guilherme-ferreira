const languageButtons = document.querySelectorAll(".language-button");

function setLanguage(language) {
    const selectedTranslations = translations[language];

    document.documentElement.lang = language === "pt" ? "pt-BR" : language;
    document.title = selectedTranslations.pageTitle;

    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription) {
        metaDescription.setAttribute("content", selectedTranslations.metaDescription);
    }

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const translationKey = element.getAttribute("data-i18n");
        element.textContent = selectedTranslations[translationKey];
    });

    document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
        const translationKey = element.getAttribute("data-i18n-alt");
        element.setAttribute("alt", selectedTranslations[translationKey]);
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
        const translationKey = element.getAttribute("data-i18n-aria-label");
        element.setAttribute("aria-label", selectedTranslations[translationKey]);
    });

    languageButtons.forEach((button) => {
        const isActive = button.getAttribute("data-lang") === language;
        button.classList.toggle("active", isActive);
    });

    localStorage.setItem("portfolioLanguage", language);
}

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        const selectedLanguage = button.getAttribute("data-lang");
        setLanguage(selectedLanguage);
    });
});

const savedLanguage = localStorage.getItem("portfolioLanguage");

if (savedLanguage && translations[savedLanguage]) {
    setLanguage(savedLanguage);
} else {
    setLanguage("en");
}
